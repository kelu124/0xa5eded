//!
//! \file       main.c
//!
//! \brief      ADC software for the 0xA5EDED board
//!

//---------------------------------------------------------------------------
// INCLUDES
//--------------------------------------------------------------------------
#include "adc/adc.h"

//---------------------------------------------------------------------------
// MAIN FUNCTION
//---------------------------------------------------------------------------

int main()
{
    stdio_init_all();
    sleep_ms(10);
    set_sys_clock_khz(250000, true);
    pio_adc_init();
    while (true)
    {
        adc();
        sleep_ms(100);
    }
}
